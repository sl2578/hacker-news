package branch.hackernews.JSONObject;

public class User {
    private String id;
    private int created;
    private int karma;
    private String about;

    public String getId() {
        return id;
    }

    public int getCreated() {
        return created;
    }

    public int getKarma() {
        return karma;
    }

    public String getAbout() {
        return about;
    }
}
